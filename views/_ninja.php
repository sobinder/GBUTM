<p>Features for Plugin with Ninja Form </p>
<ol>
	<li> Saved UTM parameters in Cookie for 30 days </li>
	<li> Add your hidden field into your form </li>
	<li> Put ninja utm parameters shortcodes on hidden field default values </li>
	<li> Use Following Shortcodes for UTM :  <br> 
			UTM source: [nj_utm_source] <br> 
			UTM medium: [nj_utm_medium]  <br> 
			UTM term: [nj_utm_term]   <br> 
			UTM content: [nj_utm_content] <br> 
			UTM campaign: [nj_utm_campaign] <br> 
			GCLID : [nj_gclid] <br> 
	</li> 
	<li> To display utm fields in mail, use the {field:ADMINISTRATION FIELD KEY} in notification Message field. Example {field:utm_source}.</li>
</ol>