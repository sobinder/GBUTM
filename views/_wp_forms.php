<p>Features for Plugin with WP Forms </p>
<ol>
	<li> Saved UTM parameters in Cookie for 30 days </li>
	<li> Use UTM parameters WP Form with add classes in hidden fileds send these UTM parameters in mail </li>
	<li> Use Following classes for UTM :  <br> 
			UTM source: utm_source <br> 
			UTM medium: utm_medium  <br> 
			UTM term: utm_term   <br> 
			UTM content: utm_content <br> 
			UTM campaign: utm_campaign <br> 
			GCLID : gclid <br> 
	</li> 
	<li> To display utm fields, use the {field_id=ID} tag in notification Message field. Example {field_id="3"}.</li>
</ol>