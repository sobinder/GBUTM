<p>Features for Plugin with Gravity Form </p>
<ol>
	<li> Saved UTM parameters in Cookie for 30 days </li>
	<li> Use UTM parameters in Gravity Form with add dynamic parameters in hidden fileds send these UTM parameters in mail </li>
	<li> Use Following classes for UTM :  <br> 
			UTM source: utm_source <br> 
			UTM medium: utm_medium  <br> 
			UTM term: utm_term   <br> 
			UTM content: utm_content <br> 
			UTM campaign: utm_campaign <br> 
			GCLID : gclid <br> 
	</li> 
	<li> To display UTM fields in Mail, use the {field name:ID} Tag in notification Message field. example:{utm_source:4}</li>
</ol>